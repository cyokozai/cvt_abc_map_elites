# Fitness function
fitness(x::Float64) = x >= 0 ?  1.0 / (1.0 + x) : abs(1.0 + x)

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~#
# Noise function
noise(gene::Vector{Float64}) = FIT_NOISE ? [rand(RNG) < p_noise ? x + (rand(RNG) * 2 * r_noise - r_noise) * (UPP - LOW)/2.0 : x for x in gene] : gene

